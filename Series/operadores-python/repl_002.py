# Asignación
number = 5
print(number)
# Suma aumentada
number += 5  # Equivale a number = number + 5
print(number)
# Resta aumentada
number -= 5  # Equivale a number = number - 5
print(number)
# Multiplicación aumentada
number *= 3  # Equivale a number = number * 3
print(number)
# División aumentada
number /= 3  # Equivale a number = number / 3
print(number)
# Módulo aumentado
number %= 3  # Equivale a number = number % 3
print(number)
# Potencia aumentada
number **= 3  # Equivale a number = number ** 3
print(number)
# División entera aumentada
number //= 3  # Equivale a number = number // 3
print(number)
