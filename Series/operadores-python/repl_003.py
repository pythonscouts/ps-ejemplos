# Concatenación de cadenas
print("Hola, " + "mundo!")
print("Python " + "es " + "genial")
# Concatenación aumentada en cadenas
greeting = "Hola"
greeting += ", mundo!"
print(greeting)
message = "Python"
message += " es genial"
print(message)
# Concatenación de listas
print([1, 2, 3] + [4, 5, 6])
print(["a", "b"] + ["c", "d"])
# Concatenación aumentada en listas
numbers = [1, 2, 3]
numbers += [4, 5, 6]
print(numbers)
letters = ["a", "b"]
letters += ["c", "d"]
print(letters)
